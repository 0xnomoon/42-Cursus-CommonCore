#include "libft.h"

int		ft_atoi(const char *str)
{
	int i;
	int neg;

	neg = 1;
	i = 0;
	while (*str != '\0' && (*str == '\t' || *str == '\n' || *str == '\r' ||
			*str == '\v' || *str == '\f' || *str == ' '))
		str++;
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
			neg = -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		i = i * 10 + (*str - 48);
		str++;
	}
	return (i * neg);
}
